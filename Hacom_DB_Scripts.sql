CREATE DATABASE db_Aplicacion_Movil_HACOM
USE db_Aplicacion_Movil_HACOM

CREATE TABLE tb_Usuario (
ID_Usuario INT AUTO_INCREMENT NOT NULL,
Nombre_Usuario NVARCHAR(25) NOT NULL, 
Telefono INT UNIQUE CHECK(LENGTH (Telefono) = 8) NOT NULL,

PRIMARY KEY (ID_Usuario)
);

CREATE TABLE tb_Vehiculo (
ID_Vehiculo INT AUTO_INCREMENT NOT NULL,
Placa NVARCHAR(6) CHECK(LENGTH(Placa) = 6) NOT NULL,
Punto_Color NVARCHAR(6) NOT NULL CHECK(LENGTH(Punto_Color) = 6 AND Punto_Color IN ('FF0000', '00FF00', '0000FF', '000000')),
Etiqueta NVARCHAR(20),

PRIMARY KEY (ID_Vehiculo)
);

CREATE TABLE tb_Ubicacion (
ID_Ubicacion INT AUTO_INCREMENT NOT NULL,
Nombre NVARCHAR(20) NOT NULL,
Longitud DECIMAL(9, 6) NOT NULL,
Latitud DECIMAL(9, 6) NOT NULL,

PRIMARY KEY (ID_Ubicacion)
);

CREATE TABLE tb_Lugar (
ID_Lugar INT AUTO_INCREMENT NOT NULL,
Nombre NVARCHAR(30) NOT NULL,
Telefono INT NOT NULL CHECK(LENGTH(Telefono) = 8),
Descripcion NVARCHAR(50),
Longitud DECIMAL(9, 6) NOT NULL,
Latitud DECIMAL(9, 6) NOT NULL,

PRIMARY KEY (ID_Lugar)
);

CREATE TABLE tb_Ubicacion_Vehiculo (
ID_Ubicacion_Vehiculo INT AUTO_INCREMENT NOT NULL,
ID_Vehiculo INT NOT NULL,
ID_Ubicacion INT NOT NULL,

PRIMARY KEY (ID_Ubicacion_Vehiculo),
FOREIGN KEY (ID_Vehiculo) REFERENCES tb_Vehiculo(ID_Vehiculo),
FOREIGN KEY (ID_Ubicacion) REFERENCES tb_Ubicacion(ID_Ubicacion)
);

